package dto;

public class TravelSleepDTO {
	private String s_num;
	private String s_name; 
	private String s_do;
	private String s_si;
	private String s_context;
	
	public TravelSleepDTO(){
		
	}

	public String getS_num() {
		return s_num;
	}

	public void setS_num(String s_num) {
		this.s_num = s_num;
	}

	public String getS_name() {
		return s_name;
	}

	public void setS_name(String s_name) {
		this.s_name = s_name;
	}

	public String getS_do() {
		return s_do;
	}

	public void setS_do(String s_do) {
		this.s_do = s_do;
	}

	public String getS_si() {
		return s_si;
	}

	public void setS_si(String s_si) {
		this.s_si = s_si;
	}

	public String getS_context() {
		return s_context;
	}

	public void setS_context(String s_context) {
		this.s_context = s_context;
	}
	
}
