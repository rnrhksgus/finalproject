package dto;

public class TravelEatDTO {
	private int e_num;
	private String e_name;
	private String e_do;
	private String e_si;
	private String e_context;

	public TravelEatDTO() {

	}

	public int getE_num() {
		return e_num;
	}

	public void setE_num(int e_num) {
		this.e_num = e_num;
	}

	public String getE_name() {
		return e_name;
	}

	public void setE_name(String e_name) {
		this.e_name = e_name;
	}

	public String getE_do() {
		return e_do;
	}

	public void setE_do(String e_do) {
		this.e_do = e_do;
	}

	public String getE_si() {
		return e_si;
	}

	public void setE_si(String e_si) {
		this.e_si = e_si;
	}

	public String getE_context() {
		return e_context;
	}

	public void setE_context(String e_context) {
		this.e_context = e_context;
	}

}
