package dto;

public class TravelFriendDTO {
	private int u_num;
	private int f_num;
	private int f_name;
	
	public TravelFriendDTO(){
		
	}

	public int getU_num() {
		return u_num;
	}

	public void setU_num(int u_num) {
		this.u_num = u_num;
	}

	public int getF_num() {
		return f_num;
	}

	public void setF_num(int f_num) {
		this.f_num = f_num;
	}

	public int getF_name() {
		return f_name;
	}

	public void setF_name(int f_name) {
		this.f_name = f_name;
	}
	

}
